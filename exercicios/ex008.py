#Faça um Programa que converta metros para centímetros.

metros = int(input('Quantos metros deseja converter? '))
centimetros = metros*100
milimetros = metros*1000

print(f'O seu valor de metros convertido para centrimetros é de: {centimetros}cm')
print(f'O seu valor de metros convertido para milimetros é de: {milimetros}mm')

