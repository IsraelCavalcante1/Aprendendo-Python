# O prêmio de 780.000,00 será dividido entre três ganhadores de um concurso. Sendo que da quantia total:
#      1ª receberá 46%.
#      2º receberá 32%,
#      3º receberá o restante


premio = 780000.00
primeiro = premio*0.46
segundo = premio*0.32
terceiro = premio*0.22


print(f'O primeiro ganhador receberá: {primeiro:.2f}')
print(f'O segundo ganhador receberá: {segundo:.2f}')
print(f'O terceiro ganhador receberá {terceiro:.2f}')


